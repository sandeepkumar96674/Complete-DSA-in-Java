import java.util.*;
public class do_while {
    public static void main(String[] args) {
        Scanner in = new Scanner (System.in);
        /*
        Do-While loop Syntax:
        do{

        }while(Condition);
         */
        int num=1;
        do {
            System.out.println (num);
            num++;
        }while(num<=5);
    }
}
